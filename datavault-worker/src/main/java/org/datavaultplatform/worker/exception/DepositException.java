package org.datavaultplatform.worker.exception;

public class DepositException extends Exception {

	private static final long serialVersionUID = -7055642396272941368L;

	public DepositException() {
		super();
	}

	public DepositException(String message, Throwable cause) {
		super(message, cause);
	}

	public DepositException(String message) {
		super(message);
	}

	public DepositException(Throwable cause) {
		super(cause);
	}

	
}
